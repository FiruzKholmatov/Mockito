package ru.netology;

public class MovieItem {

    private int id;
    private String movieName;
    private int price;
    private int rating;



    public MovieItem(int id, String movieName, int price, int rating) {
        this.id = id;
        this.movieName = movieName;
        this.price = price;
        this.rating = rating;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMovieName() {
        return movieName;
    }

    public void setMovieName(String movieName) {
        this.movieName = movieName;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }
}
